x = int(input("Введите число Х: "))
a = []

for i in range(0, n):
    a.append(random.randint(1, 100))
print(a)

newA = set(a)
minVal = sorted(newA)[0]
diff = abs(x-minVal)

for val in sorted(newA):
    if abs(x-val) < diff:
        diff = abs(x-val)
